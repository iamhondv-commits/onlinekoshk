function initRejectPayoutFormListener() {
    const form = document.getElementById('rejectPayoutForm');
    if (!form) return;

    form.addEventListener('submit', async function (e) {
        e.preventDefault();
        e.stopImmediatePropagation();

        const requestId = document.getElementById('rejectPayoutRequestId').value;
        const reason = document.getElementById('rejectPayoutReason').value.trim();
        const submitBtn = form.querySelector('button[type="submit"]');

        if (!requestId) return;

        try {
            let client = window.supabaseClient;
            if (!client && window.supabase && typeof SUPABASE_URL !== 'undefined') client = window.supabase.createClient(SUPABASE_URL, SUPABASE_ANON_KEY);

            if (submitBtn) submitBtn.disabled = true;

            const { data: updatedReq, error: reqErr } = await client
                .from('payout_requests')
                .update({ status: 'rejected', rejection_reason: reason })
                .eq('id', requestId)
                .eq('status', 'pending')
                .select();

            if (reqErr) throw reqErr;

            if (!updatedReq || updatedReq.length === 0) {
                showAdminToast('⚠️ تمت معالجة هذا الطلب سابقاً في قاعدة البيانات.', 'warning');
                const modalEl = document.getElementById('rejectPayoutModal');
                const modal = bootstrap.Modal.getInstance(modalEl);
                if (modal) modal.hide();
                loadPayoutRequests();
                return;
            }

            showAdminToast('✅ تم رفض الطلب وتقوم قاعدة البيانات بإرجاع الرصيد تلقائياً!', 'success');

            const modalEl = document.getElementById('rejectPayoutModal');
            const modal = bootstrap.Modal.getInstance(modalEl);
            if (modal) modal.hide();

            loadPayoutRequests();
            loadUsersList();
            loadAdminStats();

        } catch (err) {
            console.error('Error rejecting payout:', err);
            showAdminToast('خطأ أثناء الرفض: ' + err.message, 'error');
        } finally {
            if (submitBtn) submitBtn.disabled = false;
        }
    });
}

