        document.getElementById('msgModalContent').innerText = msg.message || 'لا يوجد نص.';

        const replyBtn = document.getElementById('msgModalReplyBtn');
        if (replyBtn) {
            replyBtn.href = `mailto:${msg.email}?subject=${encodeURIComponent('رد على: ' + (msg.subject || 'استفسارك في أونلاين كشك'))}`;
        }

