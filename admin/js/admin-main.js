function initLiveSiteIframeInteraction() {
    const iframe = document.getElementById('liveSiteIframe');
    if (iframe) {
        iframe.addEventListener('load', function () {
            try {
                const iframeDoc = iframe.contentDocument || iframe.contentWindow.document;
                if (iframeDoc) {
                    // تحويل الإطار إلى رؤية حية نظيفة وتفاعلية مثل الموقع تماماً بدون فريم برتقالي
                    const style = iframeDoc.createElement('style');
                    style.innerHTML = `
                        /* رؤية حية ونظيفة للموقع للمعاينة فقط */
                        body { user-select: auto !important; }
                    `;
                    iframeDoc.head.appendChild(style);
                }
            } catch (err) {
                console.warn('تنبيه معاينة الـ iFrame:', err.message);
            }
        });
    }
}

