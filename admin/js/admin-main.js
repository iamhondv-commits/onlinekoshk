// 🌟 دالة توليد الكلمات المفتاحية أوتوماتيكياً (Auto Keywords Engine)
function generateAutoKeywords(title, description) {
    const combinedText = `${title || ''} ${description || ''}`;
    const cleanText = combinedText.replace(/[^\w\u0621-\u064A\s]/gi, ' ');
    const words = cleanText.split(/\s+/).filter(w => w.length > 2);
    const uniqueWords = [...new Set(words)];
    return uniqueWords.slice(0, 15).join(', ');
}

// 🌟 التنقل بين Tabs الإدخال بنمط Shopify Style
window.switchShopifyTab = function(type, sectionKey, btnEl) {
    const parentModal = btnEl.closest('.modal-body');
    if (!parentModal) return;

    const allTabs = parentModal.querySelectorAll('.shopify-tab-btn');
    allTabs.forEach(b => b.classList.remove('active'));
    btnEl.classList.add('active');

    const allSections = parentModal.querySelectorAll('.shopify-tab-content');
    allSections.forEach(s => s.classList.add('d-none'));

    const targetSection = parentModal.querySelector(`#${type}TabSection_${sectionKey}`);
    if (targetSection) {
        targetSection.classList.remove('d-none');
    }
};

