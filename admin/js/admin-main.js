// 🌟 جلب التحليلات الحقيقية الفعلية من قاعدة البيانات Supabase (analytics_events)
async function loadAnalyticsData() {
    if (typeof ApexCharts === 'undefined') return;

    try {
        let client = window.supabaseClient;
        if (!client && window.supabase && typeof SUPABASE_URL !== 'undefined') {
            client = window.supabase.createClient(SUPABASE_URL, SUPABASE_ANON_KEY);
        }

        if (!client) return;

        // جلب الأحداث المسجلة في جدول التحليلات
        const { data: analyticsEvents, error } = await client
            .from('analytics_events')
            .select('*')
            .order('created_at', { ascending: false });

        if (error) throw error;

        // 1. تجميع بيانات حركة الزيارات والضغطات اليومية
        const dailyViewsMap = {};
        const dailyClicksMap = {};
        const deviceMap = { 'Desktop': 0, 'Mobile': 0 };

        (analyticsEvents || []).forEach(evt => {
            const dateKey = new Date(evt.created_at).toLocaleDateString('ar-EG', { month: 'short', day: 'numeric' });
            
            if (evt.event_type === 'click') {
                dailyClicksMap[dateKey] = (dailyClicksMap[dateKey] || 0) + 1;
            } else {
                dailyViewsMap[dateKey] = (dailyViewsMap[dateKey] || 0) + 1;
            }

            if (evt.device_type === 'Mobile') {
                deviceMap['Mobile']++;
            } else {
                deviceMap['Desktop']++;
            }
        });

        const categories = Object.keys(dailyViewsMap).length > 0 ? Object.keys(dailyViewsMap).reverse() : ['اليوم'];
        const viewsData = categories.map(k => dailyViewsMap[k] || 0);
        const clicksData = categories.map(k => dailyClicksMap[k] || 0);

        // 2. رسم المخطط البياني للحركة اليومية (Traffic Area Chart)
        const trafficEl = document.getElementById('trafficChart');
        if (trafficEl) {
            trafficEl.innerHTML = '';
            const trafficOptions = {
                chart: { type: 'area', height: 300, toolbar: { show: false } },
                colors: ['#9333ea', '#10b981'],
                series: [
                    { name: 'زيارات الصفحات الفردية', data: viewsData.length > 0 ? viewsData : [1] },
                    { name: 'نقرات زر الشراء والكاش باك', data: clicksData.length > 0 ? clicksData : [0] }
                ],
                xaxis: { categories: categories },
                stroke: { curve: 'smooth', width: 2 },
                fill: { type: 'gradient', gradient: { shadeIntensity: 1, opacityFrom: 0.4, opacityTo: 0.05 } }
            };
            trafficChartInstance = new ApexCharts(trafficEl, trafficOptions);
            trafficChartInstance.render();
        }

        // 3. رسم المخطط البياني لتوزيع أجهزة الزوار (Devices Donut Chart)
        const locationEl = document.getElementById('locationChart');
        if (locationEl) {
            locationEl.innerHTML = '';
            const totalDesktop = deviceMap['Desktop'] || 1;
            const totalMobile = deviceMap['Mobile'] || 0;

            const locationOptions = {
                chart: { type: 'donut', height: 300 },
                colors: ['#9333ea', '#3b82f6'],
                series: [totalDesktop, totalMobile],
                labels: ['أجهزة الكمبيوتر (Desktop)', 'الهواتف الذكية (Mobile)']
            };
            locationChartInstance = new ApexCharts(locationEl, locationOptions);
            locationChartInstance.render();
        }

        // 4. تعبئة جدول أكثر المنتجات والصفحات زيارة
        const topTable = document.getElementById('analyticsTopPagesTable');
        if (topTable) {
            const pageStatsMap = {};
            (analyticsEvents || []).forEach(evt => {
                const path = evt.page_path || '/';
                if (!pageStatsMap[path]) {
                    pageStatsMap[path] = { views: 0, clicks: 0 };
                }
                if (evt.event_type === 'click') {
                    pageStatsMap[path].clicks++;
                } else {
                    pageStatsMap[path].views++;
                }
            });

            const sortedPages = Object.keys(pageStatsMap);
            if (sortedPages.length > 0) {
                topTable.innerHTML = sortedPages.map(page => {
                    const stats = pageStatsMap[page];
                    const conversionRate = stats.views > 0 ? Math.round((stats.clicks / stats.views) * 100) : 0;
                    return `
                        <tr>
                            <td class="fw-bold text-start text-purple">${page}</td>
                            <td class="fw-bold">${stats.views} زيارة</td>
                            <td class="text-success fw-bold">${stats.clicks} نقرة</td>
                            <td><span class="badge bg-purple-soft text-purple fs-9">${conversionRate}% تحويل</span></td>
                        </tr>
                    `;
                }).join('');
            } else {
                topTable.innerHTML = `<tr><td colspan="4" class="text-muted py-3">جاري تجميع أحداث الزيارات والنقرات المباشرة...</td></tr>`;
            }
        }

    } catch (err) {
        console.warn('تنبيه جلب التحليلات الحقيقية:', err.message);
    }
}

