function showAdminToast(message, type = 'success') {
    const existing = document.querySelector('.admin-toast-custom');
    if (existing) existing.remove();

    const toast = document.createElement('div');
    toast.className = `admin-toast-custom admin-toast-${type} animate-slide-in`;
    toast.innerHTML = `
        <div class="d-flex align-items-center gap-2">
            <i class="bi ${type === 'success' ? 'bi-check-circle-fill' : 'bi-exclamation-triangle-fill'} fs-5"></i>
            <span class="fw-medium fs-8">${message}</span>
        </div>
        <button type="button" class="btn-close btn-close-white ms-auto fs-9" onclick="this.parentElement.remove()"></button>
    `;
    document.body.appendChild(toast);

    setTimeout(() => { toast.remove(); }, 4000);
}

function showAdminConfirm({ title = 'تأكيد الإجراء', message = 'هل أنت تأكد من الاستمرار؟', confirmText = 'نعم، تأكيد', cancelText = 'إلغاء', type = 'danger' }) {
    return new Promise((resolve) => {
        const oldModal = document.getElementById('adminCustomConfirmModal');
        if (oldModal) oldModal.remove();

        const btnClass = type === 'danger' ? 'btn-danger' : 'btn-purple';

        const modalHtml = `
            <div class="modal fade show d-block" id="adminCustomConfirmModal" tabindex="-1" style="background: rgba(15, 23, 42, 0.6); backdrop-filter: blur(4px); z-index: 1090;">
                <div class="modal-dialog modal-dialog-centered modal-sm">
                    <div class="modal-content border-0 shadow-lg rounded-4 overflow-hidden">
                        <div class="modal-body p-4 text-center">
                            <h6 class="fw-bold text-dark mb-2">${title}</h6>
                            <p class="text-muted fs-8 mb-4">${message}</p>
                            <div class="d-flex gap-2 justify-content-center">
                                <button type="button" class="btn btn-light rounded-pill px-3 fs-8 fw-semibold flex-fill" id="btnConfirmCancel">${cancelText}</button>
                                <button type="button" class="btn ${btnClass} rounded-pill px-3 fs-8 fw-semibold flex-fill" id="btnConfirmOk">${confirmText}</button>
                            </div>
                        </div>
                    </div>
                </div>
            </div>
        `;

        document.body.insertAdjacentHTML('beforeend', modalHtml);

        const modalEl = document.getElementById('adminCustomConfirmModal');
        const btnOk = document.getElementById('btnConfirmOk');
        const btnCancel = document.getElementById('btnConfirmCancel');

        const closeConfirm = (result) => {
            modalEl.classList.remove('show');
            setTimeout(() => {
                modalEl.remove();
                resolve(result);
            }, 200);
        };

        btnOk.addEventListener('click', () => closeConfirm(true));
        btnCancel.addEventListener('click', () => closeConfirm(false));
    });
}